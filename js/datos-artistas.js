// Calculte proportion of paintings by genre
function propObrasGenero(generosJson){
	var l = Object.keys(generosJson.generos).length;
	var dict =[];
	var obras = [l];
	var total = 0; 
	for (i =0; i<l; i++){
		obras[i] = generosJson.generos[i].total; 
		total = total + obras[i];
	}
	
	
	for(j=0; j<l; j++){
		obras[j] = (obras[j]/total);//*100; 
		dict.push({
			genero: generosJson.generos[j].name,
			sname: generosJson.generos[j].short_name,
			proporcion: obras[j]
		})
	}
	return dict; 
}

// Sort proportions
function ordenarProporciones(datos){
	var len = Object.keys(datos).length;
	var j = 0, k = 0; 
	var imp = [];
	var resto = [];
	for(i = 0; i<len; i++){
		if(datos[i].proporcion<0.03){
			resto[j]= datos[i];
			j = j+1; 
		}else{
			imp[k] = datos[i];
			k = k+1; 
		}
	}
	
	for (x = 0; x<imp.length; x++){
		y = x; 
		aux = imp[x];
		temp = imp[x].proporcion;
		while(y>0 && imp[y-1].proporcion<temp){
			imp[y] = imp[y-1];
			y--; 
		}
		imp[y] = aux; 
	}
	
	var total = [imp,resto];
	return total;
	
}
