// Calculte total of artists by genre
function calcularArtistas(artistsJson, name){
	total = 0; 
	var l = Object.keys(artistsJson.artists).length;
	for (i = 0; i<l; i++){
		for(j = 0; j<artistsJson.artists[i].genre.length;j++){
			if(artistsJson.artists[i].genre[j] == name){
				total = total + 1;
			}
		}	
	}
	return total; 
}

